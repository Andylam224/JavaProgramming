package Day5;

public class DoWhileLoopDemo {

    public static void main(String[] args){

        //1......10
        int i=1;
        do{
            System.out.println(i);
            i++;

        }while(i<=10);

        //10......1

        int u=10;
        do{
            System.out.println(u);
            u--;
        }while(u>=1);
    }
}
