package Day4;

public class WhileVsDoWhileLoop {


    public static void main(String[] args){

        int i=1;

        while(i<=5){
            System.out.println(i);
            i++;
        }

        int j=1;
        do{
            System.out.println(j);
            j++;
        }while(j<=5);

            //do while vs while, if condition is false, do while will run at least once,
    }

}
