package Day4;

public class EvenOrOddNumber {

    public static void main(String[] args) {


        int num = 15;
        //if a number divided by 2 returns 0, it is an even number
        //if a number divided by 2 return a non-zero, it is an odd number
        if (num % 2 == 0) {
            System.out.println(num + " is an even number");
        } else {
            System.out.println(num + " it is an odd number");
        }





    }
}
